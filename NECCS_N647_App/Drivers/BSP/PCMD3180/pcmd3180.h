typedef PCMD3180_StatusTypeDef (*PCMD3180_WriteRegFn)(void *context,
                                                      uint8_t address7,
                                                      uint8_t reg,
                                                      uint8_t value);
typedef PCMD3180_StatusTypeDef (*PCMD3180_ReadRegFn)(void *context,
                                                     uint8_t address7,
                                                     uint8_t reg,
                                                     uint8_t *value);
typedef void (*PCMD3180_DelayMsFn)(void *context, uint32_t delay_ms);
typedef void (*PCMD3180_SetShutdownFn)(void *context, uint8_t asserted);

typedef struct
{
    void *context;
    PCMD3180_WriteRegFn write_reg;
    PCMD3180_ReadRegFn read_reg;
    PCMD3180_DelayMsFn delay_ms;
    PCMD3180_SetShutdownFn set_shutdown;
} PCMD3180_BusTypeDef;

typedef struct
{
    uint8_t address7;
    uint8_t current_page;
    uint8_t configured;
    PCMD3180_BusTypeDef bus;
} PCMD3180_HandleTypeDef;

typedef struct
{
    uint8_t start_slot;
    uint8_t input_channel_mask;
    uint8_t output_channel_mask;
    uint8_t logical_channel_count;
    PCMD3180_SlotWidthTypeDef slot_width;
    uint32_t sample_rate_hz;
    uint8_t tdm_slots_per_bus;
    uint32_t expected_bclk_hz;
    uint8_t tdm_tx_offset;
    uint8_t invert_bclk;
    uint8_t invert_fsync;
    PCMD3180_PdmClockDividerTypeDef pdmclk_divider;
    uint8_t pdmin_edge_mask;
    PCMD3180_HighPassFilterTypeDef hpf_select;
    uint8_t enable_micbias;
    uint8_t verify_writes;
} PCMD3180_ConfigTypeDef;

