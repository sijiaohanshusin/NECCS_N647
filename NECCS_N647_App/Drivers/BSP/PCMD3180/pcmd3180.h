/*
 * PCMD3180 register driver.
 *
 * This layer is MCU-agnostic. Provide I2C, delay and optional shutdown GPIO
 * callbacks through PCMD3180_BusTypeDef, or use pcmd3180_hal.c on STM32 HAL.
 *
 * NECCS V2 array notes:
 * - Design source: H7_Original/firmware/NECCS/docs/array_32ch_design.html
 *   and array_32ch_coords.csv.
 * - The new hardware plan is 4 x PCMD3180, split across two synchronous TDM
 *   buses: U1/U2 on Bus A and U3/U4 on Bus B.
 * - 32ch at 48 kHz is the default real-time imaging mode.
 * - 16ch at 192 kHz is the high-quality core-array mode. It uses only four
 *   active PDM inputs on each PCMD3180 so the per-bus TDM bandwidth remains
 *   practical.
 *
 * The sample rate is not generated by this driver in the default slave-clock
 * setup. The host SAI or an external clock source must provide matching
 * BCLK/FSYNC. The mode helpers below only keep the PCMD3180 slot map, channel
 * enable masks and expected clock metadata in one place.
 *
 * Typical V2 bring-up flow:
 * 1. Initialize the STM32 I2C and the shared MIC_SHDNZ GPIO.
 * 2. Use PCMD3180_HAL_BusInit() to bind this portable driver to HAL I2C.
 * 3. Initialize one handle and call PCMD3180_HardwareReset() once, because the
 *    four PCMD3180 devices normally share the same shutdown line.
 * 4. Call PCMD3180_ConfigureArrayMode(handles, NULL, &bus,
 *    PCMD3180_ARRAY_MODE_32CH_48K) for the default V2 imaging mode.
 * 5. Configure the host SAI independently from mode_config.expected_bclk_hz.
 *
 * Example:
 *   PCMD3180_ArrayModeConfigTypeDef mode;
 *   PCMD3180_GetArrayModeConfig(PCMD3180_ARRAY_MODE_32CH_48K, &mode);
 *   // mode.expected_bclk_hz is 12.288 MHz per TDM bus for 48 kHz, 16-bit,
 *   // 16-slot capture.
 */

